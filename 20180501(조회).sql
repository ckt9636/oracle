--KOPO_PRODUCT_VOLUME 테이블에서
--ST0002 상품의 201515, 201516년도 제품만 조회하세요

SELECT * FROM KOPO_PRODUCT_VOLUME
WHERE 1=1
AND PRODUCTGROUP = 'ST0002'
AND YEARWEEK IN ('201515' , '201516')
;

SELECT * FROM KOPO_PRODUCT_VOLUME
WHERE 1=1
AND PRODUCTGROUP = 'ST0002'
AND YEARWEEK > '201514'
AND YEARWEEK< '201517'
;

SELECT * FROM KOPO_PRODUCT_VOLUME
WHERE 1=1
AND PRODUCTGROUP = 'ST0002'
        AND (YEARWEEK = '201515'
            OR YEARWEEK = '201516')
;

